flavors = ["beet", "green", "mango"]
# Create a list of flavors
for flavor in flavors:
    print(f"We have {flavor} juice!")
print(f"Yum!")
print("What would you like?")
# The for loop iterates over each item in the list and assigns it to the variable 'flavor'
# The print statement inside the loop uses an f-string to format the output

# The loop will print each flavor in the list

# the for / in syntax is used to iterate over a sequence (like a list, tuple, or string) in Python
# the for / in syntax in a for loop tells Python to repeat actions for each element in a given iteraable object, an interable object returns one element at a time
# lists, tuples, and strings are all iterable objects
# The for loop will continue until it has gone through all the items in the list
# the indentation of the print statement indicates that it is part of the for loop
# you can have more than one line of code inside the for loop, just make sure to indent them all
