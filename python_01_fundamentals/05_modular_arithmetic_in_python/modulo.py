# Modulo is a mathematical operation that finds the remainder of division of one number by another.
# In Python, the modulo operator is represented by the percent sign (%).    
# It is used to perform the modulo operation between two numbers.
# The syntax for the modulo operator is:
#   result = a % b
# where 'a' is the dividend and 'b' is the divisor.
# The result will be the remainder of the division of 'a' by 'b'.
# Example of modulo operation
a = 10
b = 3
result = a % b
print(f"The result of {a} % {b} is: {result}")  # Output: The result of 10 % 3 is: 1

# The modulo operator can also be used with negative numbers.
# Example with negative numbers
a = -10
b = 3
result = a % b
print(f"The result of {a} % {b} is: {result}")  # Output: The result of -10 % 3 is: 2
# The result is positive because the modulo operation always returns a non-negative remainder.

# The modulo operator can also be used with floating-point numbers.
# Example with floating-point numbers
a = 10.5
b = 3.2
result = a % b
print(f"The result of {a} % {b} is: {result}")  # Output: The result of 10.5 % 3.2 is: 0.9
# The result is the remainder of the division of 10.5 by 3.2.


